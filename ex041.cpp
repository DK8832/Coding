#include <iostream>
using namespace std;
int main()
{
    int num;
    cin >> num;
    switch(num) {
    case 1:
        cout << "one" << endl;
        cout << "uno" << endl;
        cout << "un" << endl;
        break;
    case 2:
        cout << "two" << endl;
        cout << "due" << endl;
        cout << "doux" << endl;
        break;
    case 3:
        cout << "three" << endl;
        cout << "tre" << endl;
        cout << "trois" << endl;
        break;
    default:
        cout << "etc" << endl;
        break;

    }

    return 0;
}
